import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata = {
  title: "Toist - Simple Todo List",
  description: "A simple browser-based todo list application built with Next.js",
};

/**
 * Root layout component that wraps all pages in the application.
 * Sets up global fonts (Geist Sans and Geist Mono) and provides
 * the basic HTML structure with proper font variables.
 */
export default function RootLayout({ children }) {
  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        {children}
      </body>
    </html>
  );
}
